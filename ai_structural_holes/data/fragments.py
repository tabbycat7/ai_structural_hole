def choose(options: Sequence[str], variant_seed: Optional[str], family: str) -> str:
    """从素材列表中确定性抽取一条。

    variant_seed 为 None 时返回首条（规范条目）；否则用 `seed|family` 作
    随机种子抽取——同一 seed 下可复现，不同 family 相互独立。
    """
    if variant_seed is None:
        return options[0]
    rng = random.Random(f"{variant_seed}|{family}")
    return options[rng.randrange(len(options))]
